#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
/*
Listenin üzerinde dolanır ve ‘f’ fonksiyonunu
listenin her elemanının içeriğine uygular.
*/
